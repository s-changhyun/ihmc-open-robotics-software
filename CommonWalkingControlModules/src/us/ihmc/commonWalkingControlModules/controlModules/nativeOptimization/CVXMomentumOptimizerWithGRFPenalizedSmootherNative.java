   int solve(double[] A, double[] b, double[] C, double[] Js, double[] ps, double[] Ws, double[] Lambda, double[] Qrho, double[] c, double[] rhoMin,
         double[] WRho, double[] rhoPrevious, double[] WRhoSmoother, double[] rhoPreviousMean, double[] WRhoCoPPenalty) throws NoConvergenceException
   {
      int numberOfIterations;
      synchronized (solveConch)
      {
         setBufferToArray(ADoubleBuffer, A);
         setBufferToArray(bDoubleBuffer, b);
         setBufferToArray(CDoubleBuffer, C);
         setBufferToArray(JsDoubleBuffer, Js);
         setBufferToArray(psDoubleBuffer, ps);
         setBufferToArray(WsDoubleBuffer, Ws);
         setBufferToArray(LambdaDoubleBuffer, Lambda);
         setBufferToArray(QrhoDoubleBuffer, Qrho);
         setBufferToArray(cDoubleBuffer, c);
         setBufferToArray(rhoMinDoubleBuffer, rhoMin);
         setBufferToArray(WRhoDoubleBuffer, WRho);

         setBufferToArray(rhoPreviousDoubleBuffer, rhoPrevious);
         setBufferToArray(WRhoSmootherDoubleBuffer, WRhoSmoother);

         setBufferToArray(rhoPreviousMeanDoubleBuffer, rhoPreviousMean);
         setBufferToArray(WRhoCoPPenaltyDoubleBuffer, WRhoCoPPenalty);

         numberOfIterations = solveNative();

         rhoDoubleBuffer.rewind();
         rhoDoubleBuffer.get(rho);

         vdDoubleBuffer.rewind();
         vdDoubleBuffer.get(vd);

         cvxMomentumOptimizerWithGRFPenalizedSmootherNativeOutput.setJointAccelerations(vd);
         cvxMomentumOptimizerWithGRFPenalizedSmootherNativeOutput.setOptVal(getOptValNative());
         cvxMomentumOptimizerWithGRFPenalizedSmootherNativeOutput.setRho(rho);
         cvxMomentumOptimizerWithGRFPenalizedSmootherNativeOutput.setNumberOfIterations(numberOfIterations);
      }

      if (numberOfIterations < 0)
      {
         throw new NoConvergenceException();
      }
      return numberOfIterations;
   }

   public static void main(String[] args) throws NoConvergenceException
   {
      double[] A = new double[wrenchLength * nDoF];
      double[] b = new double[wrenchLength];
      double[] C = new double[wrenchLength]; // diagonal
      double[] Js = new double[nDoF * nDoF];
      double[] ps = new double[nDoF];
      double[] Ws = new double[nDoF]; // diagonal
      double[] Lambda = new double[nDoF]; // diagonal
      double[] Qrho = new double[wrenchLength * rhoSize];
      double[] c = new double[wrenchLength];
      double[] rhoMin = new double[rhoSize];
      double[] WRho = new double[rhoSize];

      double[] rhoPrevious = new double[rhoSize];
      double[] WRhoSmoother = new double[rhoSize];

      double[] rhoPreviousMean = new double[rhoSize];
      double[] WRhoCoPPenalty = new double[rhoSize];

      load_default_data(A, b, C, Js, ps, Ws, Lambda, Qrho, c, rhoMin, WRho, rhoPrevious, WRhoSmoother, rhoPreviousMean, WRhoCoPPenalty);

      CVXMomentumOptimizerWithGRFPenalizedSmootherNative momentumOptimizerNative = new CVXMomentumOptimizerWithGRFPenalizedSmootherNative(nDoF, rhoSize);

      long time = System.nanoTime();
      int nSolves = 10000;
      for (int i = 0; true; i++)
      {
         if (i % nSolves == 0)
         {
            double deltaTimeSeconds = (System.nanoTime() - time) / 1e9;
            double timePerSolveMillis = deltaTimeSeconds / nSolves * 1e3;
            System.out.println("10000 iterations took " + deltaTimeSeconds + " seconds. Time per solve (ms): " + timePerSolveMillis);

            //          System.out.println(Arrays.toString(momentumOptimizerNative.getRho()));

            time = System.nanoTime();
         }

         momentumOptimizerNative.solve(A, b, C, Js, ps, Ws, Lambda, Qrho, c, rhoMin, WRho, rhoPrevious, WRhoSmoother, rhoPreviousMean, WRhoCoPPenalty);
      }
   }

